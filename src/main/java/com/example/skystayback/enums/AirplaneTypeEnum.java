package com.example.skystayback.enums;

public enum AirplaneTypeEnum {
    JET,           // Avión a reacción de uso general.
    PRIVATE_JET,   // Avión a reacción de uso privado o ejecutivo.
    COMMERCIAL,    // Avión de pasajeros usado en aerolíneas comerciales.
    CARGO_PLANES,  // Avión diseñado para el transporte de mercancías.
    MILITARY,      // Avión de uso militar, como cazas o bombarderos.
    TURBOPROP,     // Avión con hélices impulsadas por turbinas (uso comercial y regional).
    REGIONAL_JET,  // Avión de menor capacidad, usado en rutas cortas.
}